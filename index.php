<script type="text/javascript">
    $("#enviar").click(function(){

        formulario = document.getElementById('formulario_enviar');
        for (var i = 0; i < formulario.length; i++) {
            if (formulario[i].name != "") {
                formulario[i].setCustomValidity('');

                if (!formulario[i].checkValidity()) {
                    formulario[i].reportValidity();
                    return;
                }
            }
        }   

        var formData = new FormData(document.getElementById("formulario_enviar"));

        $(".g-recaptcha").hide();
        $("#enviar").hide();
        $("#gif_cargando").show();
        $("#alertaOk").hide();
        $("#alertaError").hide();  

        $.ajax({
            url: 'enviar.php' ,
            type: "post",
            dataType: "html",
            data: formData,
            cache: false,
            contentType: false,
            processData: false,
            success: function(data) {
                data = JSON.parse(data);

                switch(data){
                    case "Ok":
                    $("#alertaOk").show();
                    $("#alertaError").hide(); 
                    $("#nombre").val("");
                    $("#email").val("");
                    $("#telefono").val("");
                    $("#empresa").val("");
                    $("#consulta").val("");
                    break;
                    case "Error":
                    $("#alertaOk").hide();
                    $("#alertaError").show();    
                    $("#alertaError").html("¡ERROR! Al enviar la consulta. Inténtelo nuevamente.");                
                    break;
                    case "Captcha":
                    $("#alertaOk").hide();
                    $("#alertaError").show();   
                    $("#alertaError").html("¡ERROR! En la validación del reCAPTCHA. Inténtelo nuevamente.");                 
                    break;
                }

                $(".g-recaptcha").show();
                grecaptcha.reset();
                $("#enviar").show();
                $("#gif_cargando").hide();

            },
        }); 
    });
</script>
